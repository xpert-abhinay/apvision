              <h3>Components of Solar System</h3>
               <p>
                The following components constitute a typical rooftop solar system installed on an individual home –
                    <ul class="li-font-li">
                        <li>  Solar panels / solar modules</li>
                        <li>Inverter</li>
                        <li>Solar mounting structures</li>
                        <li>DC Cables</li>
                        <li>AC Cables</li>
                        <li>DC Combiner Box</li>
                        <li>AC Combiner Box</li>
                        <li>Lightning Arrester</li>
                        <li>Earthing kits & Earthing cables</li>
                        <li>Conduit pipes or cable trays for routing cables</li>
                        <li>MC4 connectors</li>
                        <li> Electrical connection components</li>
                    </ul>
               
                <p>The above components constitute a grid-connected solar system, where solar power is generated only when the system is connected to the electricity grid. These are the most common type of system installed on residential rooftops in cities & towns where grid shutdown is rare. Off-grid or hybrid systems can generate & supply power even when not connected to the electricity grid. Here batteries are also required to store the electricity generated. These are used in rural areas not connected to electricity grid.</p>
               </p> 
             <h3 id="Choosing-Location"> Choosing the Location, Direction & Angle for Solar Installation</h3>  
            <div class="row">

                <div class="col-md-6">
                    <p class="text-justify">The most important factor in selection of location of solar panel installation is the availability of shadow-free area. For a typical rooftop installation on an individual home, we encounter a number of shadow casting objects such as nearby trees, adjacent houses or buildings, mobile towers, water tanks, staircase rooms or mumties, satellite dishes, etc. Although we might feel that an area is free of shadow to the naked eye, it is not possible to say with certainty that there shall be no shadows at all times of day and throughout the entire year.

